public class Multiplication {
    public static void main(String[] args) {
        for(int i = 1; i<=4; i++){
            for(int k = 1; k<=10; k++){
                System.out.println(i+"*"+k +"="+(i*k));
            }
            System.out.println("\n"); // "/n" gives a new line between table
        }
    }
}
